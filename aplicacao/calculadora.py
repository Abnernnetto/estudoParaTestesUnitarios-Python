
def soma(numero_a, numero_b):
    return numero_a + numero_b


def subtracao(numero_a, numero_b):
    return numero_a - numero_b


def multiplicacao(numero_a, numero_b):
    return numero_a * numero_b


def divisao(numero_a, numero_b):
    return numero_a / numero_b

